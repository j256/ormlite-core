/**
 * The SQL data types that are supported. These are basically an enumeration of the constants in java.sql.Types.
 * 
 * @author graywatson
 */
public enum SqlType {

	STRING,
	LONG_STRING,
	DATE,
	BOOLEAN,
	BYTE,
	SHORT,
	INTEGER,
	LONG,
	FLOAT,
	DOUBLE,
	SERIALIZABLE,
	BLOB,
	UNKNOWN,
	// end
	;
}
